    private void questionIDKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_questionIDKeyPressed
        // TODO add your handling code here:
        questionID.setTransferHandler(null);
        if (Character.isDigit(evt.getKeyChar()) || evt.getKeyChar() == KeyEvent.VK_BACK_SPACE || evt.getKeyChar() == KeyEvent.VK_DELETE) {
            questionID.setEditable(true);
        } else {
            questionID.setEditable(false);
        }
    }//GEN-LAST:event_questionIDKeyPressed


