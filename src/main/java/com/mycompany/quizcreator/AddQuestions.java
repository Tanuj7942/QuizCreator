    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        if (questionID.getText().isEmpty()
                || this.question.getText().isEmpty()
                || firstOption.getText().isEmpty()
                || secondOption.getText().isEmpty()
                || thirdOption.getText().isEmpty()
                || fourthOption.getText().isEmpty()
                || correctOptions.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "One or more fields are left blank", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            Question question = new Question();
            question.setQuestionID(Integer.parseInt(questionID.getText()));
            question.setQuestion(this.question.getText());
            question.setFirstOption(firstOption.getText());
            question.setSecondOption(secondOption.getText());
            question.setThirdOption(thirdOption.getText());
            question.setFourthOption(fourthOption.getText());

            if (firstCorrectOption.isSelected()) {
                question.setCorrectOption(firstOption.getText());
            } else if (secondCorrectOption.isSelected()) {
                question.setCorrectOption(secondOption.getText());
            } else if (thirdCorrectOption.isSelected()) {
                question.setCorrectOption(thirdOption.getText());
            } else if (fourthCorrectOption.isSelected()) {
                question.setCorrectOption(fourthOption.getText());
            }

            QuestionDatabase database = new QuestionDatabase();
            try {
                database.addQuestion(question);

                JOptionPane.showMessageDialog(this, "Question Addded Successfully!!", "Confirmation Message", JOptionPane.INFORMATION_MESSAGE);

                questionID.setText("");
                this.question.setText("");
                firstOption.setText("");
                secondOption.setText("");
                thirdOption.setText("");
                fourthOption.setText("");
                correctOptions.clearSelection();

            } catch (SQLIntegrityConstraintViolationException ex) {
                LOG.error(ex + "\n");
                JOptionPane.showMessageDialog(this, ex, "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_addBtnActionPerformed

